function H = u2Hmine(u1, u2)
%UNTITLED8 Summary of this function goes here
%   u1 = 3x4
%   u2 = 3x4
%   [ x2, x2, x3, x4 ]
%   [ y2, y2, y3, y4 ]
%   [ 1, 1, 1, 1, 1]
%   [ x2' ...
%

u2_x = u2(1, :);
u2_y = u2(2, :);

A = [
    0, 0, 0, -u1(:,1)', u2_y(1) * u1(:,1)';
    -u1(:,1)', 0, 0, 0, u2_x(1) * u1(:,1)';
    0, 0, 0, -u1(:,2)', u2_y(2) * u1(:,2)';
    -u1(:,2)', 0, 0, 0, u2_x(2) * u1(:,2)';
    0, 0, 0, -u1(:,3)', u2_y(3) * u1(:,3)';
    -u1(:,3)', 0, 0, 0, u2_x(3) * u1(:,3)';
    0, 0, 0, -u1(:,4)', u2_y(4) * u1(:,4)';
    -u1(:,4)', 0, 0, 0, u2_x(4) * u1(:,4)';
];

h = null(A);


if numel(h) ~= 9
    H = [];
    return;
end
%H = h;
H = reshape(h, 3, 3)';


end
